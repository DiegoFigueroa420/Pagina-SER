// Helper functions for game content
window.getRandomGameContent = function(gameType, category) {
    const content = window.gameContent[gameType];
    if (!content) return null;
    
    // Return appropriate content based on game type and category
    switch (gameType) {
        case 'memory':
            return {
                symbols: shuffleArray([...content.abundanceSymbols]),
                words: shuffleArray([...content.prosperityWords]),
                colors: shuffleArray([...content.energyColors])
            };
            
        case 'word':
            const randomWord = content.abundanceWords[Math.floor(Math.random() * content.abundanceWords.length)];
            return { ...randomWord };
            
        case 'matching':
            const pairs = category === 'energia' ? content.energyPairs : content.abundancePairs;
            return shuffleArray([...pairs]).slice(0, 4);
            
        case 'puzzle':
            const message = content.abundanceMessages[Math.floor(Math.random() * content.abundanceMessages.length)];
            return {
                message: message,
                pieces: shuffleArray(message.split(' '))
            };
            
        case 'selection':
            return content.abundanceChoices[Math.floor(Math.random() * content.abundanceChoices.length)];
            
        case 'sequence':
            return content.abundanceSteps[Math.floor(Math.random() * content.abundanceSteps.length)];
            
        case 'pattern':
            return content.abundancePatterns[Math.floor(Math.random() * content.abundancePatterns.length)];
            
        default:
            return null;
    }
};

// Utility function to shuffle arrays
function shuffleArray(array) {
    const newArray = [...array];
    for (let i = newArray.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [newArray[i], newArray[j]] = [newArray[j], newArray[i]];
    }
    return newArray;
}

// Export for global access
window.shuffleArray = shuffleArray;
